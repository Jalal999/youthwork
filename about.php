        <p style="text-align: justify;"><b>The International Visegrad Fund</b> is an international donor organization
established by the governments of the Visegrad Group (V4) countries (the
Czech Republic, Hungary, Poland and Slovakia). Its aim is to support closer
understanding and cooperation among people in the V4 region, as well as
between the V4 region and other countries and regions, particularly in
the non-EU neighbourhood of the Eastern Partnership countries and in the
Western Balkans. The fund does fosters the development of civil society by
financing joint grant projects and by awarding university scholarships and
artist residencies. The fund’s annual budget of €8 million consists of equal
contributions by the V4 governments.</p>
